#include <stdio.h>

int main(void) {

  // Declaration of integer variables.
  int value1, value2, sum;
  // Assigning values to the declared variable.
  value1 = 50;
  value2 = 25;
  // Adding of two variable with declared values.
  sum = value1 + value2;
  // Printing the processed variables.
  printf("The sum of %i and %i is %i\n", value1, value2, sum);

  return 0;
}
